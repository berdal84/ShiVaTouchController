function TouchController.isUnderComponent ( nX, nY, nMinX, nMinY, nMaxX, nMaxY )
--------------------------------------------------------------------------------
	
    if ( nX < nMinX or nX > nMaxX or nY < nMinY or nY > nMaxY ) then
        return false
    end
    
    return true
	
--------------------------------------------------------------------------------
end
